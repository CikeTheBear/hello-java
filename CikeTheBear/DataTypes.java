package CikeTheBear;
public class DataTypes {
    public static void main(String[] args) {
        
        // Primitive Data Types
        // Primitive data types are the most basic data types in Java.
        // They are not objects and represent single values.
        // The eight primitive data types in Java are:
        // byte, short, int, long, float, double, char, and boolean.

        byte byteValue = 100; // 8-bit signed integer
        short shortValue = 10000; // 16-bit signed integer
        int intValue = 100000; // 32-bit signed integer
        long longValue = 10000000000L; // 64-bit signed integer

        float floatValue = 10.5f; // 32-bit floating point
        double doubleValue = 20.99; // 64-bit floating point

        char charValue = 'A'; // single 16-bit Unicode character
        boolean booleanValue = true; // true or false

        int myInt = 10;

        double myDouble = 50.5;

        char myChar = 'C';

        boolean myBoolean = true;
    }
}
