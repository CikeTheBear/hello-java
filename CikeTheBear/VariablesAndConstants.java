package CikeTheBear;
public class VariablesAndConstants {
    public static void main(String[] args) {
        
// Variables

// Variables are used to store data that can change during the execution of a program
// In Java, variables must be declared with a specific type
// The type of a variable determines what kind of data it can hold
// The most common types are int (integer), double (decimal), char (character), and String (text)
// It's a good practice to use lowercase letters for variable names


        String name = "Carlos";

        int age = 33;

        double height = 1.79;

        char size = 'L';

        boolean isMarried = true;


// We can use the var keyword to declare variables without specifying their type
// The type will be inferred from the value assigned to the variable

        var lastName = "De Almeida";

        var weight = 90.5;

        var isSingle = false;

        var examNote = 'S';
        



// Constants

// It's a good practice to use uppercase letters for constants
// Constants are variables whose value cannot change during the execution of a program
// In Java, we can declare constants using the final keyword
// Constants are useful to store values that are known at compile time, such as mathematical constants or configuration values


        final String EMAIL = "carlos@gmail.com";

















    }
}
